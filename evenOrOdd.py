#3.	Even or Odd
# Prompt the user to input an integer
evenOrOdd = int(input())

# Check if the number is even or odd
if evenOrOdd % 2 == 0:
    # If the number is divisible by 2 with no remainder, it's even
    print("even")
else:
    # If the number is not divisible by 2, it's odd
    print("odd")

